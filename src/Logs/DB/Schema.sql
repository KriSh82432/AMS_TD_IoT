CREATE TABLE Users (
  Name varchar(25) NOT NULL,
  RegisterNo VARCHAR(20) UNIQUE NOT NULL,
  UID varchar(8) UNIQUE NOT NULL
);

CREATE TABLE EntryLogs (
  EntryTime INT DEFAULT NULL,
  UID VARCHAR(8) DEFAULT NULL,
  ExitTime INT DEFAULT NULL,
  FOREIGN KEY (UID) REFERENCES Users(UID)
);

ALTER TABLE Users ADD COLUMN Domain ENUM(1, 2, 3, 4, 5, 6, 7, 8) DEFAULT NULL;